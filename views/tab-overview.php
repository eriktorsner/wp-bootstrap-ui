<p>
    This tools helps you create the needed Wp-Boostrap settings for an active WordPress installation.

    <br><br>
    The settings consists of two main parts:

    <h3>1. appsettings.json</h3>
    The appsettings.json file is the main configuration file used by Wp-Bootstrap. Use the tab 'Appsettings.json'
    for help genererating or modifiying this file.
    <br>
    


    <h3>2. WP-CFM settings</h3>
    Wp-Bootstrap depends on WP-CFM to transfer a lot of the WordPress options between installs. This requires the
    WP-CFM plugin to be installed.

</p>